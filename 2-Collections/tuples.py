# Immutable versions of lists representd by ( )

# Empty tuple
tuple = ( )
print(tuple)

# Initialized tuple
tuple = (1, 2, 3, 4, "fdsfsdf", "sfsdf")
print(tuple)

# We cannot modify the contents of a tuple once initialised
# tuple[0] = 3 not allowed

# Access ele by index
print(tuple[0])

# Tuples inside a list
list = [(6, 5), (6, 7)]
