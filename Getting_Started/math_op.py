# Mathematical operations
num = 5.23

# Exponent operation using ** and pow()
print(pow(num, 1 / 3))
print(2 ** 3)  # equal to 2^3

# Absolute value
print(abs(num))

# Maximum and Minimum of all the numbers
print("Max : " + str(max(1, 5, 6)), "  Min : ", min(1, 5, 6))

# Round operation
print(round(4.56), "  ", round(7.3))

# importing a library
from math import *

# Floor and ceil operatons
print("Floor : ", floor(4.78), "  Ceil : ", ceil(4.78))

# Sqaure roots
print(sqrt(25.04))
